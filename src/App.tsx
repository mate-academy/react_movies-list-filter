import React, { useEffect, useState } from 'react';
import './App.scss';
import { MoviesList } from './components/MoviesList';
import moviesFromServer from './api/movies.json';

export const App: React.FC = () => {
  const [query, setQuery] = useState('');
  const [visibleMovies, setVisibleMovies] = useState<Movie[]>(moviesFromServer);

  useEffect(() => {
    const loweredQuery = query.toLowerCase();
    const movies = moviesFromServer.filter(({ title, description }) => {
      const loweredTitle = title.toLowerCase();
      const loweredDescription = description.toLowerCase();

      return (loweredTitle.includes(loweredQuery)
        || loweredDescription.includes(loweredQuery));
    });

    setVisibleMovies(movies);
  }, [query]);

  const handleChane = (event: React.ChangeEvent<HTMLInputElement>) => {
    setQuery(event.target.value);
  };

  return (
    <div className="page">
      <div className="page-content">
        <div className="box">
          <div className="field">
            <label htmlFor="search-query" className="label">
              Search movie
            </label>

            <div className="control">
              <input
                type="text"
                id="search-query"
                className="input"
                value={query}
                onChange={handleChane}
                placeholder="Type search word"
              />
            </div>
          </div>
        </div>

        <MoviesList movies={visibleMovies} />
      </div>

      <div className="sidebar">
        Sidebar goes here
      </div>
    </div>
  );
};
